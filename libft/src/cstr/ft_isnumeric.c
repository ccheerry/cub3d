#include "lft.h"

inline t_u8	ft_isnumeric(const char *__restrict__ s)
{
	if (!s || !*s)
		return (0);
	while (*s)
	{
		if (!(*s >= '0' && *s <= '9'))
			return (0);
		s++;
	}
	return (1);
}
